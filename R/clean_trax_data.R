clean_trax_data <- function(dataset) {
  dataset |>
    # fix date before arranging
    fix_date() |>

    arrange(across(all_of(c("FXS ID", "Visit Date", "Event Name")))) |>
    remove_unneeded_records() |>
    dplyr::relocate("Visit Date", .after = "Event Name") |>
    dplyr::relocate("FXS ID", .before = "Event Name") |>
    # no need to call `clean_head_tremor_onset() |>`;
    # in trax, it's already numeric

    combine_tremor() |>
    fix_tremor_onsets() |>

    fix_onset_age_vars() |>

    fix_iq() |>

    clean_kinesia() |>
    # includes BDS, MMSE
    make_vars_numeric(regex = "score", ignore.case = TRUE) |>
    make_vars_numeric(regex = "Full Scale IQ", ignore.case = TRUE) |>

    make_vars_numeric(regex = "Purdue pegboard") |>
    make_vars_numeric(regex = "SCL90") |>
    categorize_SCL90() |>


    create_any_cancer() |>

    create_any_autoimmune() |>


    categorize_BDS() |>

    # make_vars_numeric(regex = "BDS-2 Total Score") |> # nolint: commented_code_linter
    # make_vars_numeric(regex = "MMSE total score") |> # nolint: commented_code_linter

    # `Drugs used` is unstructured text, with typos; unusable
    # fix_drugs_used() |> # nolint: commented_code_linter

    # categorize_MMSE() |> # nolint: commented_code_linter

    # fix_ApoE() |> # nolint: commented_code_linter

    fix_CGG() |>


    # dplyr::relocate(contains("CGG"), .after = contains("ApoE")) |> # nolint: commented_code_linter

    fix_FXTAS_stage() |>
    clean_fxtas_dx() |>

    fix_demographics() |>

    clean_MRI_vars() |>

    clean_cantab() |>

    clean_SCID() |>

    fix_drinks_per_day() |>

    # create SCID and MRI domains
    create_scid_domains() |>
    create_mri_domains() |>

    # cases and controls
    define_cases_and_controls() |>

    # Ataxia
    clean_ataxia() |>

    fix_factors() |>

    # clean/recode race and ethnicity
    categorize_primary_race() |>
    # clean_ethnicity() |> # fixed upstream during data import
    create_race_ethnicity() |>

    drop_levels(
      except = exceptions_to_droplevels
    ) |>
    clean_gender() |>
    dplyr::mutate(`MMSE total score*` = NA) |>
    fix_parkinsonian_features() |>
    add_labels()


}
